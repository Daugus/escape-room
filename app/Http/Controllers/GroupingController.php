    public function getConcepts()
    {

        $fields = ['análisis', 'biotecnología', 'medida', 'microbiología'];

        $data = new Collection();

        foreach ($fields as $field) {
            $data = $data->merge(GroupingSolution::query()
                ->where('field', '=', $field)
                ->inRandomOrder()
                ->limit(5)
                ->get());
        }

        return response()->json($data);
    }
